/*     
    npm -v ：npm版本
    npm version ：查看所有模块版本
    npm search 包 ：搜索包
    npm install 包 ：下载这个包
    *** npm install 包 --save ：下载这个包，然后把这个包引入到自己的package.json的依赖中
    npm install ：将这个包所依赖的所有包下载下来
    npm install 包 -g ：安装全局包，一般下载具有工具性质的包
    npm remove 包 ：删除包
    npm init：初始化package.json
    npm init --yes: 默认初始化
 */